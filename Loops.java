public class Loops {
	public static void main(String[] args) {
			
	//print 1-10
		
	//for loop
	for(int i=0;i<11;i++) {
		System.out.println(i);
	}
	
	//while loop
	int k=1;
	while(k<11) {
		System.out.println(k);
		k++;
	}
	
	//do while loop
	int l=1;
	do{
		System.out.println("This is do while loop");
		l++;
		
	}while(l<10);
	
	
	}

}